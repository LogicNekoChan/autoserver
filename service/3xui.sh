#!/usr/bin/env bash
set -e

##############################################################################
# 变量（host模式无需自定义网络/固定IP，已精简）
##############################################################################
CT_NAME="3xui"
IMG="bigbugcc/3x-ui:latest"
VOL_DB="xui_db"
VOL_CERT="xui_cert"

##############################################################################
# 1) 清理旧容器（如果存在）
##############################################################################
echo "[INFO] 清理旧容器..."
docker rm -f "$CT_NAME" >/dev/null 2>&1 || true

##############################################################################
# 2) 启动容器（host网络模式）
##############################################################################
echo "[INFO] 启动 3x-ui 容器（host网络模式）..."
docker run -d \
  --name "$CT_NAME" \
  --restart unless-stopped \
  --net=host \
  -v "$VOL_DB":/etc/x-ui \
  -v "$VOL_CERT":/root/cert \
  "$IMG"

##############################################################################
# 3) 等待 x-ui Web UI 就绪
##############################################################################
echo "[INFO] 等待 x-ui 启动 ..."
for i in {1..30}; do
  # host模式直接检测本机端口
  if curl -s -o /dev/null -w "%{http_code}" http://localhost:54321 | grep -q "200\|302\|303"; then
    echo "[√] x-ui 已就绪！"
    echo "访问面板：http://本机IP:54321"
    exit 0
  fi
  sleep 3
done

echo "[!] x-ui 启动超时"
exit 1
