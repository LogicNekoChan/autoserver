#!/usr/bin/env bash
# -------------------------------------------------
# menu.sh  ——  主菜单（打印 + 返回用户选择）
# -------------------------------------------------
show_menu_and_get_choice() {
    clear
    cat <<'EOF'

Docker 容器管理套件
--------------------------------
1) 容器备份
2) 数据恢复
3) 容器删除
4) 部署容器
5) 退出
--------------------------------
EOF
    local choice
    read -rp "请输入操作编号: " choice
    printf '%s\n' "$choice"
}

# 如果直接运行本脚本，则做简单测试
[[ "${BASH_SOURCE[0]}" == "${0}" ]] && show_menu_and_get_choice
